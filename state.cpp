RangeState::RangeState(const char *unique_identifier,
                       int         min,
                       int         max) : min_value(min), max_value(max),
                                          IntegerState(unique_identifier) {}

RangeState::~RangeState() {}

STATE_TYPE RangeState::getStateType() {
  return RANGE_STATE;
}

int RangeState::setValue(int value) {
  if ((value > max_value) || (value < min_value)) {
    return -1;
  } else {
    state_value = value;
  }
}

